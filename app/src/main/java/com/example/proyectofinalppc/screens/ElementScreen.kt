@Composable
fun ElementScreen(navController: NavController) {
    Scaffold {
        ElementBodyContent(navController)
    }
}

@OptIn(ExperimentalMaterial3Api::class)
@Composable
fun ElementBodyContent(navController: NavController) {
    var query by remember { mutableStateOf("") }
    var active by remember { mutableStateOf(false) }
    Column(
        modifier = Modifier.fillMaxSize(),
        verticalArrangement = Arrangement.Top,
        horizontalAlignment = Alignment.CenterHorizontally
    ) {
        SearchBar(
            query = query,
            onQueryChange = {
                query = it
            },
            onSearch = {
                active = false
            },
            active = active,
            onActiveChange = {
                active = it
            },
            placeholder = { Text(text = "Search a country") },
            leadingIcon = { Icon(imageVector = Icons.Default.Search, contentDescription = null) },
        )
        {
            if (query.isNotEmpty()) {
                val filteredCountries = countries.filter { it.contains(query, true) }
                LazyColumn {
                    items(filteredCountries) { country ->
                        Text(text = country, modifier = Modifier
                            .padding(8.dp)
                            .clickable {
                                active = false
                            })
                    }
                }
            }
        }
    }
    Column(
        modifier = Modifier.fillMaxSize(),
        verticalArrangement = Arrangement.Center,
        horizontalAlignment = Alignment.CenterHorizontally
    ) {
        Text("Element")
        Button(onClick = {
            navController.navigate(route = AppScreens.PlantDescription.route)
        }) {
            Text(text = "Plant description")
        }
        Button(onClick = {
            navController.navigate(route = AppScreens.FavoriteMenu.route)
        }) {
            Text(text = "Favorite Menu")
        }
        Button(onClick = {
            navController.navigate(route = AppScreens.MainMenu.route)
        }) {
            Text(text = "Return")
        }
    }
}

